    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        // TODO add your handling code here:

        String cid =courseId_tf.getText();
        String course = courseName_tf.getText() ;
        String cost = (coursePrice_tf.getText());

        if(cid.equals("")|| course.equals("")||cost.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Fill all row madatory!!");
        }
        else
        {
            try{
                ResultSet rs = DBLoader.executeSQL("select * from course where cname ='"+course+"'");
                if(rs.next()){
                    JOptionPane.showMessageDialog(this,"Course Already Exist !!!");

                }
                else{
                    // blankrow created
                    rs.moveToInsertRow();

                    //fill row
                    rs.updateString("cname",course);
                    rs.updateString("cost",cost);
                    //                        rs.updateString("UserName", user);

                    // insert to actual databases
                    rs.insertRow();

                    JOptionPane.showMessageDialog(this,"Course Added successfully");

                }
                courseId_tf.setText("");
                courseName_tf.setText("");
                coursePrice_tf.setText("");

            }
            catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,ex);
            }
        }
    }//GEN-LAST:event_add_btnActionPerformed

