     public void setRecordToTable(){
        ec.clear();
        try{
            ResultSet rs = DBLoader.executeSQL("select * from course");
            while(rs.next()){
                String id = rs.getString("id");
                String n = rs.getString("cname");
                String c = rs.getString("cost");
                
                ec.add(new ECourse(id,n,c));
            }
            tm.fireTableDataChanged();
            
        }
        catch(Exception ex){
            
            ex.printStackTrace();
        }
    }
    class mytablemodel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return ec.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }
        @Override
        public String getColumnName(int j) {
            String s[] ={"CourseID","Course Name","Cost of course"};
            return s[j];
        }

        @Override
        public Object getValueAt(int i, int j) {
            ECourse sd = ec.get(i);
            if(j==0)
                return sd.courseid;
            else if(j==1)
                return sd.coursename;
            else
                return sd.cost;
        }
        
    }
    
