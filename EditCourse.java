    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        // TODO add your handling code here:
        String cid =courseId_tf.getText();
        String course = courseName_tf.getText() ;
        String cost = (coursePrice_tf.getText());

        if(cid.equals("")||course.equals("")||cost.equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field are mandatory");
        }
        else{
            try
            {
                ResultSet rs = DBLoader.executeSQL("select * from course where id='"+cid+"' ");
                if(rs.next()){

                    rs.updateString("cname",course);
                    rs.updateString("cost",cost);

                    rs.updateRow();
                    JOptionPane.showMessageDialog(this,"course updated successfully");

                    courseId_tf.setText("");
                    courseName_tf.setText("");
                    coursePrice_tf.setText("");
                }
                else{
                    JOptionPane.showMessageDialog(this,"Course id are Not Matched");
                }

            }
            catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,ex);
            }
        }
    }//GEN-LAST:event_update_btnActionPerformed

